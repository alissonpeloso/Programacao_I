public class Retangulo extends Forma {
    private int altura;
    private int base;

    public void setAltura(int altura) {
        this.altura = altura;
    }
    public void setBase(int base) {
        this.base = base;
    }
    public int getAltura() {
        return altura;
    }
    public int getBase() {
        return base;
    }
}
